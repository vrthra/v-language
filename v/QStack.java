package v;
import java.util.*;

public class QStack extends Stack<Term> {
    public QStack() {
        super();
    }

    public Term push(Term t) {
        return super.push(t);
    }

    public Term pop() {
        return super.pop();
    }

    public boolean empty() {
        return super.empty();
    }

    public Term peek() {
        return super.peek();
    }
}
